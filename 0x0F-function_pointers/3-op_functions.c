#include "3-calc.h"

/**
 * op_add - add two int
 * @a: int 1
 * @b: int 2
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - diff of two int
 * @a: int 1
 * @b: int 2
 * Return: diff of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product of two int
 * @a: int 1
 * @b: int 2
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - quotient of two int
 * @a: int 1
 * @b: int 2
 * Return: quotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - quotient remainder of two int
 * @a: int 1
 * @b: int 2
 * Return: quotient remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
